/*
Its print 10 horizontal stars

 */
public class Stars10App {
    public static void main(String[] args) {
        int i = 1;
        while (i <= 10 ) {
            System.out.print("*");
            // i = i + 1 or i ++ or i +=1
            i++ ;
        }

        int j=10 ;
        while (j >=1 ) {
            System.out.println("*");
            j--;

        }
     }

}
